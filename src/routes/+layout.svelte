<script lang="ts">
	import '../app.css'
	import { onMount } from 'svelte'
	import { goto } from '$app/navigation'
	import { isEditableTarget } from '$lib/utils/keyboard'
	import NavBar from '$components/layout/NavBar.svelte'
	import BottomNav from '$components/layout/BottomNav.svelte'
	import PageTransition from '$components/layout/PageTransition.svelte'
	import Toast from '$components/ui/Toast.svelte'
	import DetailPreviewModal from '$components/modals/DetailPreviewModal.svelte'
	import { loadWatchlist } from '$lib/stores/watchlist'
	import { loadFavoritePeople } from '$lib/stores/people'

	let { children } = $props()

	onMount(async () => {
		loadWatchlist()
		loadFavoritePeople()
		// Register the service worker (client-only, generated by vite-plugin-pwa)
		const { registerSW } = await import('virtual:pwa-register')
		registerSW({ immediate: true })
	})

	let pendingG = $state(false)
	let pendingGTimer: ReturnType<typeof setTimeout> | null = null

	function clearPendingG() {
		pendingG = false
		if (pendingGTimer) {
			clearTimeout(pendingGTimer)
			pendingGTimer = null
		}
	}

	function focusSearch() {
		window.dispatchEvent(new CustomEvent('cinelist:focus-search'))
	}

	function handleGlobalKeydown(e: KeyboardEvent) {
		if (e.defaultPrevented) return
		if (isEditableTarget(e.target)) return

		// Search focus
		if (e.key === '/' && !e.metaKey && !e.ctrlKey && !e.altKey) {
			e.preventDefault()
			clearPendingG()
			focusSearch()
			return
		}
		if ((e.key === 'k' || e.key === 'K') && (e.metaKey || e.ctrlKey) && !e.altKey) {
			e.preventDefault()
			clearPendingG()
			focusSearch()
			return
		}

		// Navigation sequences (GitHub-style)
		if (pendingG) {
			const k = e.key.toLowerCase()
			if (k === 'h') {
				e.preventDefault()
				clearPendingG()
				void goto('/')
				return
			}
			if (k === 'w') {
				e.preventDefault()
				clearPendingG()
				void goto('/watchlist')
				return
			}
			if (k === 's') {
				e.preventDefault()
				clearPendingG()
				void goto('/search')
				return
			}
			clearPendingG()
			return
		}

		if (e.key === 'g' || e.key === 'G') {
			if (e.metaKey || e.ctrlKey || e.altKey) return
			pendingG = true
			if (pendingGTimer) clearTimeout(pendingGTimer)
			pendingGTimer = setTimeout(() => {
				pendingG = false
				pendingGTimer = null
			}, 900)
			return
		}
	}
</script>

<svelte:head>
	<title>CineList</title>
	<meta name="description" content="Your personal movie watchlist" />
</svelte:head>

<div class="min-h-dvh flex flex-col">
	<NavBar />
	<main class="flex-1 pb-16 sm:pb-0">
		<PageTransition>
			{@render children()}
		</PageTransition>
	</main>
	<BottomNav />
</div>

<svelte:window onkeydown={handleGlobalKeydown} />

<Toast />

<DetailPreviewModal />
